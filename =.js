// = Assignment operator
let x = 1;
y = x;

console.log(x) // 1
console.log(y) // 1

// == Equality operator (checks value)
let x = 3;
let y = "3";
if (x == y) {
console.log("Same value")
} else {
console.log("Not same value")}



// === Equality operator (checks value & type)

let x = 3;
let y = "3";
if (x === y) {
console.log("Same value and type")
} else {
console.log("Not same value or type")}
